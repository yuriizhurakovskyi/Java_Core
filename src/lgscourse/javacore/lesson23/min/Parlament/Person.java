package lgscourse.javacore.lesson23.min.Parlament;

public abstract class Person {
	private int weight;
	private int height;
	public Person(int weight, int height) {
		this.weight = weight;
		this.height = height;
	}
	public int getWeight() {
		return weight;
	}
	public int getHeight() {
		return height;
	}
	
	
}
